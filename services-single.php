                <div class="section" id="bawaan">
                  <h2>Daftar Barang Bawaan untuk Trip ke Baduy</h2>
                  <h3>Pakaian dan Alas Kaki:</h3>
                  <ul>
                    <li>Alas kaki: Sebaiknya gunakan sepatu yang nyaman untuk trekking.</li>
                    <li>Pakaian hangat: Bawa jaket, kaos kaki hangat, dan pakaian tidur yang nyaman. Suhu malam di Baduy cenderung dingin (sekitar 18-22 derajat Celcius).</li>
                    <li>Pakaian sopan: Hindari celana pendek di atas lutut untuk menghormati adat istiadat Baduy.</li>
                  </ul>
                  <h3>Perlengkapan Pribadi:</h3>
                  <ul>
                    <li>Jas hujan: Antisipasi cuaca yang tidak menentu.</li>
                    <li>Alat makan pribadi: Sendok dan tempat minum.</li>
                    <li>Headlamp: Berguna untuk aktivitas malam. Jika tidak punya, akan disediakan.</li>
                    <li>Obat-obatan pribadi: Bawa obat-obatan yang rutin Anda konsumsi.</li>
                    <li>Makanan ringan: Untuk jaga-jaga jika merasa lapar di perjalanan.</li>
                    <li>Sleeping bag: Opsional, namun sangat disarankan jika Anda sensitif terhadap suhu dingin.</li>
                    <li>Bantal tiup: Opsional, untuk menambah kenyamanan tidur.</li>
                    <li>Tracking pole: Opsional, bisa disewa dari anak-anak Baduy seharga Rp5.000.</li>
                  </ul>
                  <h3>Perlengkapan Mandi:</h3>
                  <ul>
                    <li>Sabun, pasta gigi, dan shampo: Digunakan di luar kawasan Baduy Dalam.</li>
                  </ul>
                  <h3>Lain-lain:</h3>
                  <ul>
                    <li><strong>Uang tunai:</strong> Siapkan uang tunai karena ATM hanya tersedia di Terminal Ciboleger (BRI dan Bank Banten).</li>
                    <li><strong>Makanan ringan untuk berbagi:</strong> Jika ingin memberikan oleh-oleh untuk warga Baduy.</li>
                    <li><strong>Handphone:</strong> Dimatikan saat berada di Baduy Dalam.</li>
